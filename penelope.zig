fn isStop(w: []const u8) bool {
    for (STOPS) |s| {
        if (std.mem.eql(u8, w, s)) return true;
    }
    return false;
}

fn findWord(w: []const u8) i32 {
    for (0..NWORDS) |i| {
        if (std.mem.eql(u8, VOCAB[i], w)) return @intCast(i);
    }
    return -1;
}

