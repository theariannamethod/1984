var g_prng: std.Random.DefaultPrng = undefined;

fn randf() f32 {
    return g_prng.random().float(f32);
}

fn clampf(x: f32, lo: f32, hi: f32) f32 {
    if (x < lo) return lo;
    if (x > hi) return hi;
    return x;
}

fn randn() f32 {
    const r1 = randf() + 1e-12;
    const r2 = randf() + 1e-12;
    return @sqrt(-2.0 * @log(r1)) * @cos(6.2831853 * r2);
}

fn siluf(x: f32) f32 {
    if (x > -20.0) {
        return x / (1.0 + @exp(-x));
    }
    return 0.0;
}

