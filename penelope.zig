// BPE ENCODER — text to subword token IDs
// ═══════════════════════════════════════════════════════════════

fn bpeEncode(text: []const u8, ids: []u16) usize {
    // 1. Convert text to lowercase bytes -> initial token sequence
    var seq: [MAX_BPE_SEQ]u16 = undefined;
    var len: usize = 0;
    for (text) |c| {
        if (len >= MAX_BPE_SEQ - 1) break;
        seq[len] = if (c >= 'A' and c <= 'Z') c - 'A' + 'a' else c;
        len += 1;
    }

    // 2. Apply merges in priority order
    for (0..BPE_MERGES) |mi| {
        const left = BPE_TABLE[mi][0];
        const right = BPE_TABLE[mi][1];
        const new_id: u16 = @intCast(256 + mi);

        var j: usize = 0;
        var i: usize = 0;
        while (i < len) {
            if (i < len - 1 and seq[i] == left and seq[i + 1] == right) {
                seq[j] = new_id;
                j += 1;
                i += 2; // skip next
            } else {
                seq[j] = seq[i];
                j += 1;
                i += 1;
            }
        }
        len = j;
    }

    // 3. Copy to output
    const n = if (len < ids.len) len else ids.len;
    @memcpy(ids[0..n], seq[0..n]);
    return n;
}

