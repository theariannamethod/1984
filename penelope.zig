    ffn_norm: []f32, // [DIM]         pre-FFN RMSNorm
    w_gate: []f32, // [DIM * HDIM]  SwiGLU gate (HDIM > DIM)
    w_up: []f32, // [DIM * HDIM]  SwiGLU up
    w_down: []f32, // [HDIM * DIM]  SwiGLU down
};

